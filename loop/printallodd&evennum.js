// print all odd numbers
console.log("Print all odd numbers:");
let n=20;
for(let i=1;i<=n;i=i+2){
    console.log(i);
}

// print all even numbers
console.log("print all even numbers:");
let m=20;
for(let i=0;i<=m;i=i+2){
    console.log(i);
}


// print all even numbers backwards 

console.log("backwards for even numbers:");
for(let i=20;i>=0;i=i-2){
    console.log(i);
}

// print all odd numbers backwards

console.log("backwards for odd numbers:");
for(let i=19;i>=1;i=i-2){
    console.log(i);
}
